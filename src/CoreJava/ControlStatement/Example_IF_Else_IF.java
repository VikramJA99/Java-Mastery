package CoreJava.ControlStatement;

public class Example_IF_Else_IF {
    public static void main(String[] args){
        int temp = 36;
        if (temp > 30){
            System.out.println("It very hot");
        } else if (temp > 25) {
            System.out.println("It warm");
        } else {
            System.out.println("It cold");
        }


    }
}
